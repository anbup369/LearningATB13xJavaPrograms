//To test understanding of identifier rules, since underscore is reserved keyword, but with alphabets and number it is valid variable name.
public class Lab019_Interview_QnA1 {
    public static void main(String[] args) {
        int _2 = 123; // underscore along with number is valid variable name, only underscore is not allowed.
        System.out.println(_2);
        int _1 = 123;
        int _0 = 123;
    }
}
