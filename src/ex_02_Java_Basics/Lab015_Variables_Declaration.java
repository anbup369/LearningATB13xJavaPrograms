package ex_02_Java_Basics;

public class Lab015_Variables_Declaration {
    public static void main(String[] args) {
        // ```
        //data_type variable_name =  variable_value(Literals);
         //```

        byte b = 10;
        // byte -> data type - type of container -
        // store only integer value but limited to max and min
        // -128 to 127

        // b ->  variable name -> identifier

        // = -> assignment - assign the value to the b

        // 10 -> literal -> integer value
        System.out.println(b);

    }
}
