package ex_04_Operators;

public class Lab056_Interview_char_operations {
    public static void main(String[] args) {
        short s = 10;
        char c = 'A';
        String s1 = "A";
        System.out.println(c+s);//Output: 65+10-->75
        System.out.println(c);//Output : A
        System.out.println(c+c);//Output: 65+65-->130
        System.out.println(c+s1);//Output:char A+ string A-->AA
    }
}
