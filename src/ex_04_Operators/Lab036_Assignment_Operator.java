package ex_04_Operators;

public class Lab036_Assignment_Operator {
    public static void main(String[] args) {
        int age = 65;
        // int -> data type
        // age -> identifier
        // = -> Assignment operator
        // 65 -> Literal

        // RHV to the LH --// The value on the Right Hand Side (RHS) is assigned to the variable on the Left Hand Side (LHS)
        System.out.println(age);
    }
}
