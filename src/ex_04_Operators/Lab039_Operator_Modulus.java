package ex_04_Operators;
//modulus operator %, which returns the remainder after division.
public class Lab039_Operator_Modulus {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a % b); // → 0, because 20 divided by 10 has no remainder

        System.out.println(13 % 7); // → 6, because 13 = 7×1 + 6
    }
}



