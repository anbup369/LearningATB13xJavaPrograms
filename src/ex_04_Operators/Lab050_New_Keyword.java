package ex_04_Operators;
// keyword new
public class Lab050_New_Keyword {
    public static void main(String[] args) {
        // New Operator - oops
        String name = new String("Pramod");
        // Learn this in OOPs
//        When you write new String("Pramod"), you're explicitly telling Java to:
//                - Allocate memory for a new String object
//                - Call the String constructor
//                - Store the resulting object in the reference variable name

        System.out.println(name);
    }
}
