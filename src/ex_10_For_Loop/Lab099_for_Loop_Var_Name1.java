package ex_10_For_Loop;

public class Lab099_for_Loop_Var_Name1
{ // variable name changed from i to any
    public static void main(String[] args) {
        // For Loop
        //Help you to repeat a block of code.
        // Initialization -> Condition -> Update(Increment/decrement) --> ICU
        for (int any = 0; any < 10; any++) {
            System.out.println(any);
        }
    }
}

